# Tree Traversal - to visit every node in a tree, then we will take the values and put them in a list and then return that list

# Ways to do Tree Traversal:
# Breath First Search (BFS)- start at the top than do all left and then all right
# Depth first Search (DFS) - start at bottom left then come up and come down then come up to the first item then go all the way to the bottom left of the right side tree then come up and down
#  there are 3 different ways to do Depth first Search